// Equality (==): This operator tests for equality between two values without considering the type of the operands.
// javascript

var x = 10;
var y = "10";
console.log(x == y);  // true

let x = 10;
let y = "10";
console.log(x === y);  // false

let x = 10;
let y = 20;
console.log(x != y);  // true

let x = 10;
let y = "10";
console.log(x !== y);  // true

let x = 10;
let y = 20;
console.log(x > y);  // false
console.log(x < y);  // true

let x = 10;
let y = 20;
console.log(x >= y);  // false
console.log(x <= y);  // true

