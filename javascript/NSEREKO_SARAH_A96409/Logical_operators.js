//&& This only returns true if both  conditions are true
let r=9 ,p=8
console.log(r==9&&p==8)

//|| This only returns false if both conditions are false
console.log(r>10||p>15) //Since r is not greater than 10 and p is not greater than 15 , the result is false


//! Returns the opposite or inverse  of the statement   

console.log(!(r==9)) //We know that r is indeed 9 but since we used the not operator it returned the inverse