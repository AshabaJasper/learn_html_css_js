// Comparison Operators

// Equal to ==
// this compares two varialbles to see if their value is the same and returns true if they are 
let value1 = 2
let value2 = "2"
let value3 = "Two"
let value4 = "2"
value1 == value2
console.log(value1 == value2) //this returns true since the values are the same but the datatypes are different
console.log(value3 == value4) //this returns false since the values are not the same but the datatypes are

// Not Equal to !=
// this compares two varialbles to see if their value is the same and returns true if they are 
let value5 = 2
let value6 = "2"
value5 != value6
value5 != value5
console.log(value5 != value6) //this returns false since the values are the same
console.log(value5 != value5) //this returns false since the values are the same

// Strict Equal to ===
// this compares two varialbles to see if their value is the same and returns true if they are 
let value7 = 2
let value8 = "2"
console.log(value7 === value8) //this returns false since the values are the same but the datatypes are different









