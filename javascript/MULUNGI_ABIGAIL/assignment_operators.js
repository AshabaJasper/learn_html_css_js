// subtraction assignment operators
let y= 10;
y-=5;
console.log(y);

// multiplication assignment operators
let z= 10;
z*=5;
console.log(z);

// exponentiation assignment operators
let a= 10;
a**=5;
console.log(a);

// division assignment operators
a= 10;
a/=5;
console.log(a);

// remainder assignment operators
y= 10;
y%=5;
