// Examples of Arithmetic Operators

// Addition
var x = 10;
var y = 20;
var z = x + y;
console.log(z);

// Subtraction
var x = 10;
var y = 20;
var z = x - y;
console.log(z);

// Multiplication
var x = 10;
var y = 20;
var z = x * y;
console.log(z);

// Division
var x = 10;
var y = 20;
var z = x / y;
console.log(z);

// Modulus
var x = 10;
var y = 20;
var z = x % y;
console.log(z);

// Increment
var x = 10;
x++;
console.log(x);

// Decrement
var x = 10;
x--;
console.log(x);
