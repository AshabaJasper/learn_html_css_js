// Implementation of Arithmetic Operators in Javascript
let a=8;
let b=12;

// Addition Operator (+):
let addition = a + b;
console.log(addition);

// Subtraction Operator (-):
let subtraction = b-a;
console.log(subtraction);

// Division Operator (/):
let division = a/b;
console.log(division);

//Increment Operator (++):
a = 10;
a++;
console.log(a);

// decrement Operator (--):
a = 10;
a--;
console.log(a)

// Remainder Operator (%):
let remainder = a % b;
console.log(remainder);

// Exponentitation Operator (**): 
let exponentiation = a ** b;
console.log(exponentiation);
