/**ARITHMENTIC OPERATORS */
let a = 3;
let x = (100 + 50) * a;

//ADDITION OPERATOR
/**returns the sum of the operands */
console.log(a + x); // output: 453

// SUBTRACTION OPERATOR
/**returns the difference of the operands */
console.log(a - x); // output: -447

//MULTIPLICATION
/**returns the product of the operands */
console.log(a * x); // output: 1350

//DIVISION OPERATOR
/** returns quotient of the operands */
console.log(a / x); // output: 0.006666666666666667
