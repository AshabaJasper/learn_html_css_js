//Bitwise AND Operator (&)
var a = 25;
var b = 11;
var c = a & b;
console.log(c); // 9

//Bitwise OR Operator (|)
var d = 25;
var e = 11;
var f = d | e;
console.log(f); // 31

//Bitwise XOR Operator (^)
var g = 25;
var h = 11;
var i = g ^ h;
console.log(i); // 22

//Bitwise NOT Operator (~)
var j = 25;
var k = ~j;
console.log(k); // -26

//Bitwise Left Shift Operator (<<)
var l = 25;
var m = l << 1;
console.log(m); // 50

//Bitwise Right Shift Operator (>>)
var n = 50;
var o = n >> 1;
console.log(o); // 25

//Bitwise Zero-fill Right Shift Operator (>>>)
var p = -50;
var q = p >>> 1;
console.log(q); // 2147483598