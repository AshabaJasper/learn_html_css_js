//Arithmetic Operators in JavaScript

//Addition
var x = 10;
var y = 20;
var z = x + y;
console.log(z); //30

//Subtraction
var x = 20;
var y = 10;
var z = x - y;
console.log(z); //10

//Multiplication
var x = 10;
var y = 5;
var z = x * y;
console.log(z); //50

//Division
var x = 20;
var y = 10;
var z = x / y;
console.log(z); //2

//Modulus (Remainder)
var x = 20;
var y = 7;
var z = x % y;
console.log(z); //6