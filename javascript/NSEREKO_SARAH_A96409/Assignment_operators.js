//Assignment Operators
//These are used to assign values to variables 
//Examples
//=
let f=5
console.log(f);
//+= This is used for addition to increase the value of f by a given value

console.log(f+=1);
//+- This is used for subtraction to decrease the value of f by a given value

console.log(f-=1);

//*= This is used for multiplication to multiply the value of f by a given value
console.log (f*=2)

// '/=' This is used for division to divide the value of f by a given value
console.log (f/=3)
//the above will divide f by 3

// %= This is used to derive the remainder of a value divided by f
 console.log (f%=3) //print Remainder of f when divided by 3




