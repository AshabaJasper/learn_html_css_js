// Examples of Bitwise Operators
//
// Bitwise AND
var x = 10;
var y = 20;
var z = x & y;
console.log(z);

// Bitwise OR
var x = 10;
var y = 20;
var z = x | y;
console.log(z);

// Bitwise XOR
var x = 10;
var y = 20;
var z = x ^ y;
console.log(z);

// Bitwise NOT
var x = 10;
var z = ~x;
console.log(z);

// Left shift
var x = 10;
var z = x << 2;
console.log(z);

// Right shift
var x = 10;
var z = x >> 2;
console.log(z);

// Zero fill right shift
var x = 10;
var z = x >>> 2;
console.log(z);

