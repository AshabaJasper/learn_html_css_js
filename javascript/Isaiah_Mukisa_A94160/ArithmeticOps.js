// Arithmetic operators i.e (+, %, -, +, *, **, ++, --)

// + operator
console.log(2 + 3);

// - operator
console.log(2 - 3);

// * operator
console.log(2 * 10);

// / operator
console.log(4 / 7);

// % operator
console.log(10 % 7);
// Logs the remainder of the division of 4 and 7

// Using ++
let num = 3;
num++;
console.log(num);
// ++ increments the number by 1

// using --
let num1 = 22;
num1--;
console.log(num1);
// -- decreases the number by 1

// using **
let num2 = 5;
let num0 = 2;
let power = num2 ** num0;
console.log(power);
// ** used to get exponents of numbers.
