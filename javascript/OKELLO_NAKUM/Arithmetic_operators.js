//Addition Assignment Operators
var a = 10;
a += 5;
console.log(a); // 15

//Subtraction Assignment Operators
var b = 20;
b -= 10;
console.log(b); // 10

//Multiplication Assignment Operators
var c = 2;
c *= 3;
console.log(c); // 6

//Division Assignment Operators
var d = 15;
d /= 5;
console.log(d); // 3

//Remainder Assignment Operators
var e = 17;
e %= 5;
console.log(e); // 2

//Exponential Assignment Operators
var f = 2;
f **= 3;
console.log(f); // 8