// Assignment Operator (=)
let x = 10;
console.log(x); // 10

// Addition Assignment Operator (+=)
let y = 5;
y += 5; // y = y + 5
console.log(y); // 10

// Subtraction Assignment Operator (-=)
let a = 10;
a -= 5; // a = a - 5
console.log(a); // 5

// Multiplication Assignment Operator (*=)
let b = 5;
b *= 5; // b = b * 5
console.log(b); // 25

// Division Assignment Operator (/=)
let c = 100;
c /= 10; // c = c / 10
console.log(c); // 10

// Modulus Assignment Operator (%=)
let d = 20;
d %= 3; // d = d % 3
console.log(d); // 2