// Comparison operators in Javascript are used to compare values and return a boolean value (true or false) based on the result of the comparison.

// Examples of the comparison operators in Javascript are:

// Equality (==): 
// The equality operator compares the values of two operands to see if they are equal. If the values are equal, it returns true. 
// If not, it returns false.

// Inequality (!=): 
// The inequality operator compares the values of two operands to see if they are not equal. 
// If the values are not equal, it returns true. If they are equal, it returns false.

// Greater than (>): 
// The greater than operator compares the value of the left operand to see if it is greater than the value of the right operand. 
// If it is, it returns true. If not, it returns false.

// Less than (<): 
// The less than operator compares the value of the left operand to see if it is less than the value of the right operand. 
// If it is, it returns true. If not, it returns false.

// Greater than or equal to (>=): 
// The greater than or equal to operator compares the value of the left operand to see if it is greater than or equal to the value of the right operand. 
// If it is, it returns true. If not, it returns false.

// Less than or equal to (<=): 
// The less than or equal to operator compares the value of the left operand to see if it is less than or equal to the value of the right operand. 
// If it is, it returns true. If not, it returns false.

// For example:

let x = 10;
let y = 5;
console.log(x == y); // Output: false
console.log(x != y); // Output: true
console.log(x > y);  // Output: true
console.log(x < y);  // Output: false
console.log(x >= y); // Output: true
console.log(x <= y); // Output: false

// In the example above, the equality operator returns false because the values of x and y are not equal.
//  The inequality operator returns true because the values of x and y are not equal. 
//  The greater than operator returns true because the value of x is greater than the value of y. 
//  The less than operator returns false because the value of x is not less than the value of y. 
//  The greater than or equal to operator returns true because the value of x is greater than or equal to the value of y. 
//  The less than or equal to operator returns false because the value of x is not less than or equal to the value of y.