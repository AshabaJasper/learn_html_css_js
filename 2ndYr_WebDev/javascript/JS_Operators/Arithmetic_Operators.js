// Addition Operator (+)
let x = 5;
let y = 10;
let sum = x + y;
console.log(sum); // 15

// Subtraction Operator (-)
let a = 20;
let b = 10;
let difference = a - b;
console.log(difference); // 10

// Multiplication Operator (*)
let c = 5;
let d = 10;
let product = c * d;
console.log(product); // 50

// Division Operator (/)
let e = 100;
let f = 10;
let quotient = e / f;
console.log(quotient); // 10

// Modulus Operator (%)
let g = 20;
let h = 3;
let remainder = g % h;
console.log(remainder); // 2

// Increment Operator (++)
let i = 10;
i++;
console.log(i); // 11

// Decrement Operator (--)
let j = 10;
j--;
console.log(j); // 9
