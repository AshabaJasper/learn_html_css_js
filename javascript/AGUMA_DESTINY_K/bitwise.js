//BITWISE OPERATIONS
// Bitwise AND (&):
// Example 1
let a = 5; // binary representation: 0101
let b = 3; // binary representation: 0011
console.log(a & b); // output: 1
// Example 2
let c = 6; // binary representation: 0110
let d = 2; // binary representation: 0010
console.log(c & d); // output: 2

// Bitwise OR (|):
// Example 1
let a = 5; // binary representation: 0101
let b = 3; // binary representation: 0011
console.log(a | b); // output: 7
// Example 2
let c = 6; // binary representation: 0110
let d = 2; // binary representation: 0010
console.log(c | d); // output: 6

// Bitwise XOR (^):
// Example 1
let a = 5; // binary representation: 0101
let b = 3; // binary representation: 0011
console.log(a ^ b); // output: 6

// Example 2
let c = 6; // binary representation: 0110
let d = 2; // binary representation: 0010
console.log(c ^ d); // output: 4

// Bitwise NOT (~):
// Example 1
let a = 5; // binary representation: 0101
console.log(~a); // output: -6
// Example 2
let b = -3; // binary representation: 1101
console.log(~b); // output: 2

// Bitwise Left Shift (<<):
// Example 1
let a = 5; // binary representation: 0000 0101
console.log(a << 1); // output: 10
// Example 2
let b = 6; // binary representation: 0000 0110
console.log(b << 2); // output: 24

// Bitwise Right Shift (>>):
// Example 1
let a = 5; // binary representation: 0000 0101
console.log(a >> 1); // output: 2
// Example 2
let b = 6; // binary representation: 0000 0110
console.log(b >> 2); // output: 1

// Bitwise Zero-Fill Right Shift (>>>):
// Example 1
let a = 5; // binary representation: 0000 0101
console.log(a >>> 1); // output: 2
// Example 2
let b = -6; // binary representation: 1111 1010
console.log(b >>> 2); // output: 1073741821




